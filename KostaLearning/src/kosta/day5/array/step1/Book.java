package kosta.day5.array.step1;

public class Book {
	
	private String title; 
	private String writer;
	
	public Book(String title, String writer) {
		this.title = title;
		this.writer = writer;
	}

	public String getTitle() {
		return title;
	}

	public String getWriter() {
		return writer;
	} 
	
}
