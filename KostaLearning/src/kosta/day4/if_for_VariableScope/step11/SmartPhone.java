package kosta.day4.if_for_VariableScope.step11;

public class SmartPhone {
	
	private String model; 
	private String marker;
	
	public SmartPhone(String model, String marker) {
		this.model = model;
		this.marker = marker;
	}

	public String getModel() {
		return model;
	}

	public String getMarker() {
		return marker;
	}

}
