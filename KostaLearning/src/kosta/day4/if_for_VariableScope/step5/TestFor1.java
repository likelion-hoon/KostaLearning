package kosta.day4.if_for_VariableScope.step5;

public class TestFor1 {

	public static void main(String[] args) {
		
		for(int i=10; i>0; i--) {
			System.out.print(i+" ");
		}
		
		System.out.println();
		
		for(int i=1; i<=10; i++) {
			System.out.print(11-i+" ");
		}
		
		System.out.println();
		
		
		
		
	}
}
